    private void DelElemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DelElemActionPerformed
        // TODO add your handling code here:
        int position = Integer.parseInt(DelPos.getText());
        if(position <= arr.length - 1) {
            JOptionPane.showMessageDialog(DelPos, "Element " + arr[position] + " is Deleted At Position " + position);
            arr[position] = 0;

        }
    }//GEN-LAST:event_DelElemActionPerformed

