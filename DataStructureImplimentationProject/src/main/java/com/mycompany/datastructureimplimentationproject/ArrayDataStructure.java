    private void InsertArrayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_InsertArrayActionPerformed
        // TODO add your handling code here:

        int element = Integer.parseInt(TextElem.getText());
        int position = Integer.parseInt(TextPos.getText());
           

        if(position >= 0 && position <= arr.length - 1) {
            arr[position]  = element;
            JOptionPane.showMessageDialog(TextElem, "Element " + element + " Is Inserted to The Position " + position);
        
        }
        else {
            JOptionPane.showMessageDialog(TextElem,"Insertion not Possible Choose Number between 0 to " +  (arr.length-1) + ".","Warning",JOptionPane.WARNING_MESSAGE);
        }
        TextElem.setText("");

    }//GEN-LAST:event_InsertArrayActionPerformed

