    private void QueueInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_QueueInsertActionPerformed
        // TODO add your handling code here:
        int element = Integer.parseInt(TextEnQue.getText());
        if(front < 0 && rear == queue.length - 1) {
            JOptionPane.showMessageDialog(TextEnQue, "Queue is Full Cannot insert the Element", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        else {
            ++rear;
            queue[rear] = element;
            JOptionPane.showMessageDialog(TextEnQue, "Element " + queue[rear] + " Is Inserted in Queue At " + rear);
        }
        TextEnQue.setText("");
    }//GEN-LAST:event_QueueInsertActionPerformed

