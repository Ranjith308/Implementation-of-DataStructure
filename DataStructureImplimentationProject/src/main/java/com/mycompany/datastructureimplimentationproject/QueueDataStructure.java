    private void BCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BCreateActionPerformed
        // TODO add your handling code here:
        size = Integer.parseInt(TextQue.getText());
        queue = new int[size];
        JOptionPane.showMessageDialog(TextQue, "Queue Of Size " + size + " Is Created");
    }//GEN-LAST:event_BCreateActionPerformed

    private void QueueDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_QueueDelActionPerformed
        // TODO add your handling code here:
        
        if(front == queue.length - 1 && front < 0) {
            JOptionPane.showMessageDialog(QueueDel, "Queue Is Empty Deletion Not Possible","Warning", JOptionPane.WARNING_MESSAGE);
        }
        else {
            ++front;
            JOptionPane.showMessageDialog(QueueDel, "Element " + queue[front] + " Is Deleted");
        }
    }//GEN-LAST:event_QueueDelActionPerformed

    private void BQueDisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BQueDisActionPerformed
        // TODO add your handling code here:
        QueueDis.setText("");
        if(front == queue.length - 1 || front < 0 && rear < 0) {
            JOptionPane.showMessageDialog(QueueDis, "Display Not Possible Queue Is Empty", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        else {
            for(int i = front + 1; i <= rear; i++) {
                QueueDis.setText(QueueDis.getText() + queue[i] + " ");
            }
        }
    }//GEN-LAST:event_BQueDisActionPerformed

